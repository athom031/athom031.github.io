$(document).ready(function () {

    $(window).on('scroll load', function () {
        $('#menu').removeClass('fa-times');
        $('.navbar').removeClass('nav-toggle');

        if (window.scrollY > 60) {
            document.querySelector('#scroll-top').classList.add('active');
        } else {
            document.querySelector('#scroll-top').classList.remove('active');
        }

        // scroll spy
        $('section').each(function () {
            let height = $(this).height();
            let offset = $(this).offset().top - 200;
            let top = $(window).scrollTop();
            let id = $(this).attr('id');

            if (top > offset && top < offset + height) {
                $('.navbar ul li a').removeClass('active');
                $('.navbar').find(`[href="#${id}"]`).addClass('active');
            }
        });
    });

    // smooth scrolling
    $('a[href*="#"]').on('click', function (e) {
        e.preventDefault();
        $('html, body').animate({
            scrollTop: $($(this).attr('href')).offset().top,
        }, 500, 'linear')
    });

    // <!-- load header at #header-->
    $(function(){
        $("#header").load("src/components/header/header.html");
    });

    // <!-- load footer at #footer-->
    $(function(){
        $("#footer").load("src/components/footer/footer.html");
    });

    // <!-- attach hover logo to logo in navbar -->
    var logoLink = $('#logo-link');
    var logoImg = $('#logo-img');
    logoLink.hover(
        function() {
            logoImg.attr('src', 'assets/navbar/logo-hover.png'); // path to the hover image
        },
        function() {
            logoImg.attr('src', 'assets/navbar/logo.png'); // path to the original image
        }
    );

});

